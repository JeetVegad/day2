ALTER FUNCTION PF_COUNTER (@SL INT)
RETURNS INT
AS
BEGIN 
	DECLARE @PF INT
	SET @PF = @SL *0.12;
	RETURN @PF
END

SELECT dbo.PF_COUNTER(12000)


CREATE FUNCTION PT_COUNTER(@ME INT)
RETURNS INT
AS 
BEGIN 
	DECLARE @TEXT INT
	SET @TEXT = CASE
	WHEN @ME < 6000 THEN 0
	WHEN @ME >= 6000 AND @ME<9000 THEN 80
	WHEN @ME >= 9000 AND @ME<12000 THEN 150
	WHEN @ME >=12000 THEN 200
END
RETURN @TEXT
END

